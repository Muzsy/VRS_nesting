#[inline]
pub const fn normalize_deg(deg: i32) -> usize {
    let mut norm = deg % 360;
    if norm < 0 {
        norm += 360;
    }
    norm as usize
}

#[inline]
pub fn round_div_i128(numer: i128, denom: i128) -> i64 {
    assert!(denom > 0, "denom must be positive");
    let q = if numer >= 0 {
        (numer + (denom / 2)) / denom
    } else {
        -(((-numer) + (denom / 2)) / denom)
    };

    match i64::try_from(q) {
        Ok(value) => value,
        Err(_) => {
            if q.is_negative() {
                i64::MIN
            } else {
                i64::MAX
            }
        }
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn normalize_deg_wraps_and_handles_negative() {
        assert_eq!(normalize_deg(0), 0);
        assert_eq!(normalize_deg(360), 0);
        assert_eq!(normalize_deg(721), 1);
        assert_eq!(normalize_deg(-1), 359);
        assert_eq!(normalize_deg(-721), 359);
    }

    #[test]
    fn round_div_is_half_away_from_zero() {
        assert_eq!(round_div_i128(5, 2), 3);
        assert_eq!(round_div_i128(3, 2), 2);
        assert_eq!(round_div_i128(-5, 2), -3);
        assert_eq!(round_div_i128(-3, 2), -2);
        assert_eq!(round_div_i128(4, 2), 2);
        assert_eq!(round_div_i128(-4, 2), -2);
    }
}
